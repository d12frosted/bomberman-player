module Data.Monoid.Bool where

--------------------------------------------------------------------------------

import           Data.Monoid

--------------------------------------------------------------------------------

infixr 3 .&
(.&) :: (a -> Bool) -> (a -> Bool) -> a -> Bool
p1 .& p2 = getAll <$> (All <$> p1) <> (All <$> p2)

--------------------------------------------------------------------------------

infixr 2 .|
(.|) :: (a -> Bool) -> (a -> Bool) -> a -> Bool
p1 .| p2 = getAny <$> (Any <$> p1) <> (Any <$> p2)

